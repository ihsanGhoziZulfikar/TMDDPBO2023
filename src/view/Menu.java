    private void btnAddActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddActionPerformed
        // TODO add your handling code here:
        // Insert to or get user from UserProcess
        // ...
        username = fieldNama.getText();
        if(!username.isEmpty()){
            userProcess.insertUser(username);
            
            userProcess.processUserDataByUsername(username);

            Game game = new Game();
            game.setUsername(userProcess.getUsername(0));
            game.setScore(userProcess.getScore(0));
            game.setStanding(userProcess.getStanding(0));
            bgm.stopSound(this.clip);
            this.setVisible(false);
        }else{
            System.out.println("Failed!");
            JOptionPane.showMessageDialog(null, "Username Can't Be Blank");
        }
    }//GEN-LAST:event_btnAddActionPerformed

