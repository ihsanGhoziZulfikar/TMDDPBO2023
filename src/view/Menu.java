    // set table for menu
    public void setTable(){
        // Get the data from ProcessUserData (viewmodel)
        // ...
        Object[] column = {"No", "Nama", "Score","Standing"};
        dataTable = new DefaultTableModel(null,column);
        //menampilkan tampilan
        try{
            userProcess.processUserData();
            
            //semua terkait tampilan adalah tanggung jawab View
            for(int i=0;i<userProcess.getSize();i++){
                Object[] row = new Object[4];
                row[0] = i+1;
                row[1] = userProcess.getUsername(i);
                row[2] = userProcess.getScore(i);
                row[3] = userProcess.getStanding(i);

                dataTable.addRow(row);
            }

        }catch(Exception e){
            //menampilkan error
            System.out.println(userProcess.getError());
        }
    }
    
    // return this data table
    public DefaultTableModel getTable(){
        return this.dataTable;
    }

